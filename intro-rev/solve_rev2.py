# taken from gdb strcmp
input = [0xfc,  0xfd,  0xea,  0xc0,  0xba,  0xec,  0xe8,  0xfd, 0xfb,  0xbd,  0xf7,  0xbe,  0xef,  0xb9,  0xfb,  0xf6, 0xbd,  0xc0,  0xba,  0xb9,  0xf7,  0xe8,  0xf2,  0xfd, 0xe8,  0xf2,  0xfc]

def convert(num):
  diff = 0x100 - num
  res = 0x77 - diff
  return chr(res)

"".join([convert(i) for i in input])
